function createMdxFileFromGuideByCategory() {
  guideFolders.forEach(folder =>
    guides.forEach(guide => {
      if (guide.guideCategory.title === folder.title) {
        const getStringToRender = () => {
          const stringToRender = [];

          if (guide.title) {
            const title = `# ${guide.title}\n\n`;
            stringToRender.push(title);
          }

          if (guide.description) {
            const description = `${guide.description}\n\n`;
            stringToRender.push(description);
          }

          if (guide.body?.some(x => x._type === 'callout')) {
            stringToRender.push(`import { Callout } from 'nextra-theme-docs';\n\n`);
          }

          if (guide.body) {
            const convertedBody = toMarkdown(guide.body, {
              serializers,
              projectId: '1as7cn02',
              dataset: 'production',
            });
            stringToRender.push(convertedBody);
          }

          if (guide.pageBuilder) {
            guide.pageBuilder.forEach(x => {
              stringToRender.push(`\n## ${x.title}\n`);
              stringToRender.push(`| name   | image | description | script |\n`);
              stringToRender.push(`| :----- | :----: | ----: | ----: |\n`);

              // For schem set page
              stringToRender.push(`|`);
              x.images?.map(y => {
                stringToRender.push(
                  ` ${y.heading} | ${
                    y?.asset?._ref ? `![${y.heading}](${urlForImage(y?.asset?._ref)})` : ''
                  } | ${y.description ? y.description : ''} | /schbr &${y.heading.replace(
                    /\s/g,
                    '',
                  )} \n`,
                );
                // stringToRender.push(`![${y.alt}](${urlForImage(y.asset._ref)})\n`);
              });

              // For FAQ page
              // if (x.accordionContent) {
              //   x.accordionContent?.map(y => {
              //     stringToRender.push(`### ${y.heading}\n`);
              //     stringToRender.push(`${toMarkdown(y.copy)}\n`);
              //   });
              // }
            });
          }

          //           | left   | center | right |
          // | :----- | :----: | ----: |
          // | foo    |  bar   |   baz |
          // | banana | apple  |  kiwi |

          return stringToRender.join('');
        };

        fs.writeFileSync(`./pages/${folder.path}/${guide.slug.current}.mdx`, getStringToRender());
      }
    }),
  );
}

createMdxFileFromGuideByCategory();

